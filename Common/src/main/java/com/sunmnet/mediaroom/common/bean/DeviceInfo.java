package com.sunmnet.mediaroom.common.bean;

/**
 * 安卓设备的信息
 */
public class DeviceInfo {

    private String platformIp;//平台IP
    private int platformPort;//平台端口
    private String coreIp;//核心Ip
    private int corePort;//核心端口
    private String classroomCode;
    private String classroomName;
    private String deviceCode;

    private String folderPath;//设备存储目录路径

    public DeviceInfo() {
    }

    public DeviceInfo(String platformIp, int platformPort, String coreIp, int corePort, String classroomCode, String classroomName, String folderPath) {
        this.platformIp = platformIp;
        this.platformPort = platformPort;
        this.coreIp = coreIp;
        this.corePort = corePort;
        this.classroomCode = classroomCode;
        this.classroomName = classroomName;
        this.folderPath = folderPath;
    }

    public String getPlatformIp() {
        return platformIp;
    }

    public void setPlatformIp(String platformIp) {
        this.platformIp = platformIp;
    }

    public int getPlatformPort() {
        return platformPort;
    }

    public void setPlatformPort(int platformPort) {
        this.platformPort = platformPort;
    }

    public String getCoreIp() {
        return coreIp;
    }

    public void setCoreIp(String coreIp) {
        this.coreIp = coreIp;
    }

    public int getCorePort() {
        return corePort;
    }

    public void setCorePort(int corePort) {
        this.corePort = corePort;
    }

    public String getClassroomCode() {
        return classroomCode;
    }

    public void setClassroomCode(String classroomCode) {
        this.classroomCode = classroomCode;
    }

    public String getClassroomName() {
        return classroomName;
    }

    public void setClassroomName(String classroomName) {
        this.classroomName = classroomName;
    }

    public String getFolderPath() {
        return folderPath;
    }

    public void setFolderPath(String folderPath) {
        this.folderPath = folderPath;
    }

    public String getDeviceCode() {
        return deviceCode;
    }

    public void setDeviceCode(String deviceCode) {
        this.deviceCode = deviceCode;
    }

    @Override
    public String toString() {
        return "DeviceInfo{" +
                "platformIp='" + platformIp + '\'' +
                ", platformPort=" + platformPort +
                ", coreIp='" + coreIp + '\'' +
                ", corePort=" + corePort +
                ", classroomCode='" + classroomCode + '\'' +
                ", classroomName='" + classroomName + '\'' +
                ", deviceCode='" + deviceCode + '\'' +
                ", folderPath='" + folderPath + '\'' +
                '}';
    }
}
