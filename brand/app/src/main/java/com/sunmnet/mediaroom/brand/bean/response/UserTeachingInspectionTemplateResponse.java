package com.sunmnet.mediaroom.brand.bean.response;

import android.support.annotation.NonNull;

import com.sunmnet.mediaroom.common.bean.Result;

import java.util.List;

public class UserTeachingInspectionTemplateResponse extends Result<List<UserTeachingInspectionTemplateResponse.Result>> {

    public static class Result {

        /**
         * title : string
         * value : string
         */

        private String title;
        private String value;

        public String getTitle() {
            return title;
        }

        public void setTitle(String title) {
            this.title = title;
        }

        public String getValue() {
            return value;
        }

        public void setValue(String value) {
            this.value = value;
        }

        @NonNull
        @Override
        public String toString() {
            return title != null ? title : super.toString();
        }
    }
}
