package com.sunmnet.mediaroom.brand.bean.control.table;

public class CourseTableControlAttr extends TableControlAttr {

    //课程表行列表示变换 courseHeaderRow课节数据排列为一行  courseHeaderCol课节数据排列为一列
    protected String direction;

    //单课节single 双课节double
    protected String courseStyle;

    public String getDirection() {
        return direction;
    }

    public void setDirection(String direction) {
        this.direction = direction;
    }

    public String getCourseStyle() {
        return courseStyle;
    }

    public void setCourseStyle(String courseStyle) {
        this.courseStyle = courseStyle;
    }
}
