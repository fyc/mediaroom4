package com.sunmnet.mediaroom.brand.bean;


public class UpgradeInfo {

    private String lastVersionName;
    private int lastVersionCode;
    private boolean upgradeSuccess;
    private boolean respondedResult;
    private String upgradeId;

    public String getLastVersionName() {
        return lastVersionName;
    }

    public void setLastVersionName(String lastVersionName) {
        this.lastVersionName = lastVersionName;
    }

    public int getLastVersionCode() {
        return lastVersionCode;
    }

    public void setLastVersionCode(int lastVersionCode) {
        this.lastVersionCode = lastVersionCode;
    }

    public boolean isUpgradeSuccess() {
        return upgradeSuccess;
    }

    public void setUpgradeSuccess(boolean upgradeSuccess) {
        this.upgradeSuccess = upgradeSuccess;
    }

    public boolean isRespondedResult() {
        return respondedResult;
    }

    public void setRespondedResult(boolean respondedResult) {
        this.respondedResult = respondedResult;
    }

    public String getUpgradeId() {
        return upgradeId;
    }

    public void setUpgradeId(String upgradeId) {
        this.upgradeId = upgradeId;
    }
}
