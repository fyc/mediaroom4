package com.sunmnet.mediaroom.brand.bean;


public class TableCell {

    /**
     * row : 0
     * column : 1
     * height : 100
     * width : 200
     * textStyle : {"color":"#000000","underline":false,"italic":false,"bold":false,"size":12,"textAlign":"center","font":"simsun.ttf"}
     * text :
     * fillColor : #FFFFFF
     * borderColor":"#000000"
     * borderSize":12
     */

    private int row;
    private int column;
    private int height;
    private int width;
    private TextStyleBean textStyle;
    /**
     * 表格组件数据来源
     */
    private String content;
    private String text;
    private String fillColor;
    private String borderColor;
    private int borderSize;

    private String heightLightBackgroundColor;
    private String heightLightTextColor;

    public TableCell() {
    }

    public TableCell(int row, int column) {
        this.row = row;
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public TextStyleBean getTextStyle() {
        return textStyle;
    }

    public void setTextStyle(TextStyleBean textStyle) {
        this.textStyle = textStyle;
    }

    public String getText() {
        return text;
    }

    public void setText(String content) {
        this.text = content;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getFillColor() {
        return fillColor;
    }

    public void setFillColor(String fillColor) {
        this.fillColor = fillColor;
    }

    public String getBorderColor() {
        return borderColor;
    }

    public void setBorderColor(String borderColor) {
        this.borderColor = borderColor;
    }

    public int getBorderSize() {
        return borderSize;
    }

    public void setBorderSize(int borderSize) {
        this.borderSize = borderSize;
    }

    public String getHeightLightBackgroundColor() {
        return heightLightBackgroundColor;
    }

    public void setHeightLightBackgroundColor(String heightLightBackgroundColor) {
        this.heightLightBackgroundColor = heightLightBackgroundColor;
    }

    public String getHeightLightTextColor() {
        return heightLightTextColor;
    }

    public void setHeightLightTextColor(String heightLightTextColor) {
        this.heightLightTextColor = heightLightTextColor;
    }

    public static class TextStyleBean {
        /**
         * color : #000000
         * underline : false
         * italic : false
         * bold : false
         * fontSize : 12
         * textAlign : 1
         * fontFamily : simsun.ttf
         */

        private String color;
        private boolean underline;
        private boolean italic;
        private boolean bold;
        private int fontSize;
        private String textAlign;
        private String fontFamily;

        public String getColor() {
            return color;
        }

        public void setColor(String color) {
            this.color = color;
        }

        public boolean isUnderline() {
            return underline;
        }

        public void setUnderline(boolean underline) {
            this.underline = underline;
        }

        public boolean isItalic() {
            return italic;
        }

        public void setItalic(boolean italic) {
            this.italic = italic;
        }

        public boolean isBold() {
            return bold;
        }

        public void setBold(boolean bold) {
            this.bold = bold;
        }

        public int getFontSize() {
            return fontSize;
        }

        public void setFontSize(int size) {
            this.fontSize = size;
        }

        public String getTextAlign() {
            return textAlign;
        }

        public void setTextAlign(String alignment) {
            this.textAlign = alignment;
        }

        public String getFontFamily() {
            return fontFamily;
        }

        public void setFontFamily(String font) {
            this.fontFamily = font;
        }
    }

}
