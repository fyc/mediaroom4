package com.sunmnet.mediaroom.brand.bean.item;

public class SpinnerItem<T> {

    private T key;
    private String name;

    public SpinnerItem(T key, String name) {
        this.key = key;
        this.name = name;
    }

    @Override
    public String toString() {
        return this.name;
    }

    public T getKey() {
        return key;
    }

    public String getName() {
        return name;
    }
}
