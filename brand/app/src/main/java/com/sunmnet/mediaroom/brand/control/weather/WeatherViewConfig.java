package com.sunmnet.mediaroom.brand.control.weather;

public class WeatherViewConfig {

    private int style;
    private int width;
    private int height;
    private int layoutId;

    public WeatherViewConfig(int style, int width, int height, int layoutId) {
        this.width = width;
        this.height = height;
        this.layoutId = layoutId;
        this.style = style;
    }

    public WeatherViewConfig() {
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getLayoutId() {
        return layoutId;
    }

    public int getStyle() {
        return style;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public void setLayoutId(int layoutId) {
        this.layoutId = layoutId;
    }

    public void setStyle(int style) {
        this.style = style;
    }
}
