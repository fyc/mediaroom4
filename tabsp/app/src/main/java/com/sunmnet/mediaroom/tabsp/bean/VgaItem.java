package com.sunmnet.mediaroom.tabsp.bean;

public class VgaItem {
    int selectedDrawable, unselectDrawable;
    String name, tag;
    boolean select = false;

    public boolean isSelect() {
        return select;
    }

    public void setSelect(boolean select) {
        this.select = select;
    }

    public VgaItem(int selectedDrawable, int unselectDrawable, String name, String tag) {
        this.selectedDrawable = selectedDrawable;
        this.unselectDrawable = unselectDrawable;
        this.name = name;
        this.tag = tag;
    }

    public int getSelectedDrawable() {
        return selectedDrawable;
    }

    public int getUnselectDrawable() {
        return unselectDrawable;
    }

    public String getName() {
        return name;
    }

    public String getTag() {
        return tag;
    }
}
