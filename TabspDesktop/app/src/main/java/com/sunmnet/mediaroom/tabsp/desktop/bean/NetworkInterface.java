package com.sunmnet.mediaroom.tabsp.desktop.bean;

import android.databinding.BaseObservable;
import android.databinding.Bindable;

public class NetworkInterface extends BaseObservable {
    String platformIP;
    String localIP;
    String gateway;
    String netmask = "255.255.255.0";
    String dns1 = "8.8.8.8";
    String dns2 = "8.8.4.4";

    @Bindable
    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    String state;

    @Bindable
    public String getNetworkName() {
        return networkName;
    }

    public void setNetworkName(String networkName) {
        this.networkName = networkName;
    }

    String networkName;
    String platformPort;

    @Bindable
    public String getPlatformIP() {
        return platformIP;
    }

    public void setPlatformIP(String platformIP) {
        this.platformIP = platformIP;
    }

    @Bindable
    public String getLocalIP() {
        return localIP;
    }

    public void setLocalIP(String localIP) {
        this.localIP = localIP;
    }

    @Bindable
    public String getGateway() {
        return gateway;
    }

    public void setGateway(String gateway) {
        this.gateway = gateway;
    }

    @Bindable
    public String getNetmask() {
        return netmask;
    }

    public void setNetmask(String netmask) {
        this.netmask = netmask;
    }

    @Bindable
    public String getDns1() {
        return dns1;
    }

    public void setDns1(String dns1) {
        this.dns1 = dns1;
    }

    @Bindable
    public String getDns2() {
        return dns2;
    }

    public void setDns2(String dns2) {
        this.dns2 = dns2;
    }

    @Bindable
    public String getPlatformPort() {
        return platformPort;
    }

    public void setPlatformPort(String platformPort) {
        this.platformPort = platformPort;
    }
}
